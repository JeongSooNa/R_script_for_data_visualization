## function
a <- c(2,7,5,3,1,4,6)
## sumation
sum(a)
## product
prod(a)
## maximum
max(a)
## minimum
min(a)
## difference
diff(a)
## max index
which.max(a)
## min index
which.min(a)
## range
range(a)
## mean
mean(a)
## median
median(a)
## standard deviation
sd(a)
## variance
var(a)
## quantile
quantile(a)
## standardized
scale(a)
mean(scale(a))
var(scale(a))
sd(scale(a))

